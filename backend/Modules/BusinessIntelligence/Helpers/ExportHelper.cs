using System.Text;
using System.Text.Json;
using backend.Modules.BusinessIntelligence.DTOs;

namespace backend.Modules.BusinessIntelligence.Helpers;

public static class ExportHelper
{
    public static (byte[] Content, string ContentType, string Extension) ExportToCsv(ReportDto report)
    {
        var sb = new StringBuilder();
        sb.AppendLine("Report Name,Report Type,Generated At,Generated By");
        sb.AppendLine($"\"{report.Name}\",\"{report.ReportType}\",\"{report.GeneratedAt:yyyy-MM-dd HH:mm:ss}\",\"{report.GeneratedBy}\"");
        sb.AppendLine();

        if (report.DataPayload != null)
        {
            var json = JsonSerializer.Serialize(report.DataPayload);
            using var doc = JsonDocument.Parse(json);
            var root = doc.RootElement;

            if (root.TryGetProperty("KeyMetrics", out var metricsElement) && metricsElement.ValueKind == JsonValueKind.Array)
            {
                sb.AppendLine("Metric Key,Label,Current Value,Previous Value,Percentage Change,Unit,Trend");
                foreach (var item in metricsElement.EnumerateArray())
                {
                    var key = item.GetProperty("Key").GetString();
                    var label = item.GetProperty("Label").GetString();
                    var current = item.GetProperty("CurrentValue").GetDecimal();
                    var prev = item.GetProperty("PreviousValue").GetDecimal();
                    var pct = item.GetProperty("PercentageChange").GetDouble();
                    var unit = item.GetProperty("Unit").GetString();
                    var trend = item.GetProperty("Trend").GetString();

                    sb.AppendLine($"\"{key}\",\"{label}\",{current},{prev},{pct},\"{unit}\",\"{trend}\"");
                }
            }
        }

        return (Encoding.UTF8.GetBytes(sb.ToString()), "text/csv", "csv");
    }

    public static (byte[] Content, string ContentType, string Extension) ExportToExcel(ReportDto report)
    {
        // Generates formatted TSV/Excel XML format natively readable by Excel
        var sb = new StringBuilder();
        sb.AppendLine("<?xml version=\"1.0\"?>");
        sb.AppendLine("<?mso-application progid=\"Excel.Sheet\"?>");
        sb.AppendLine("<Workbook xmlns=\"urn:schemas-microsoft-com:office:spreadsheet\"");
        sb.AppendLine(" xmlns:o=\"urn:schemas-microsoft-com:office:office\"");
        sb.AppendLine(" xmlns:x=\"urn:schemas-microsoft-com:office:excel\"");
        sb.AppendLine(" xmlns:ss=\"urn:schemas-microsoft-com:office:spreadsheet\">");
        sb.AppendLine(" <Worksheet ss:Name=\"Business Intelligence Report\">");
        sb.AppendLine("  <Table>");
        
        sb.AppendLine($"   <Row><Cell><Data ss:Type=\"String\">BusinessOS AI Report: {report.Name}</Data></Cell></Row>");
        sb.AppendLine($"   <Row><Cell><Data ss:Type=\"String\">Type: {report.ReportType} | Generated: {report.GeneratedAt:yyyy-MM-dd HH:mm:ss}</Data></Cell></Row>");
        sb.AppendLine("   <Row></Row>");
        
        sb.AppendLine("   <Row>");
        sb.AppendLine("    <Cell><Data ss:Type=\"String\">Metric Key</Data></Cell>");
        sb.AppendLine("    <Cell><Data ss:Type=\"String\">Metric Label</Data></Cell>");
        sb.AppendLine("    <Cell><Data ss:Type=\"String\">Current Value</Data></Cell>");
        sb.AppendLine("    <Cell><Data ss:Type=\"String\">Unit</Data></Cell>");
        sb.AppendLine("    <Cell><Data ss:Type=\"String\">Trend</Data></Cell>");
        sb.AppendLine("   </Row>");

        if (report.DataPayload != null)
        {
            var json = JsonSerializer.Serialize(report.DataPayload);
            using var doc = JsonDocument.Parse(json);
            var root = doc.RootElement;

            if (root.TryGetProperty("KeyMetrics", out var metricsElement) && metricsElement.ValueKind == JsonValueKind.Array)
            {
                foreach (var item in metricsElement.EnumerateArray())
                {
                    sb.AppendLine("   <Row>");
                    sb.AppendLine($"    <Cell><Data ss:Type=\"String\">{item.GetProperty("Key").GetString()}</Data></Cell>");
                    sb.AppendLine($"    <Cell><Data ss:Type=\"String\">{item.GetProperty("Label").GetString()}</Data></Cell>");
                    sb.AppendLine($"    <Cell><Data ss:Type=\"Number\">{item.GetProperty("CurrentValue").GetDecimal()}</Data></Cell>");
                    sb.AppendLine($"    <Cell><Data ss:Type=\"String\">{item.GetProperty("Unit").GetString()}</Data></Cell>");
                    sb.AppendLine($"    <Cell><Data ss:Type=\"String\">{item.GetProperty("Trend").GetString()}</Data></Cell>");
                    sb.AppendLine("   </Row>");
                }
            }
        }

        sb.AppendLine("  </Table>");
        sb.AppendLine(" </Worksheet>");
        sb.AppendLine("</Workbook>");

        return (Encoding.UTF8.GetBytes(sb.ToString()), "application/vnd.ms-excel", "xls");
    }

